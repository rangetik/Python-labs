# Варіант 8 Побудувати таблицю множення для заданого числа.

# Введення цілого числа
num = int(input("Введіть число: "))

# Виведення повідомлення
print(f"Таблиця множення для числа {num}:")

# Використовуємо range(0, 11), щоб включити числа від 0 до 10
for i in range(0, 11):
    # Виведення результатів
    print(f"{num} * {i} =", num * i)
